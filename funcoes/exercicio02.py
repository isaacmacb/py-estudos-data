def saudacao(nome):
    print(f"Olá, {nome}!")

saudacao("Carlos")  # Chama a função e passa "Carlos" como argumento

# def é usado para definir a função.
# nome é o parâmetro da função (o que a função espera receber).
# Dentro da função, print(f"Olá, {nome}!") usa o valor passado para o parâmetro nome.