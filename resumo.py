# Resumo dos Conceitos:
# Variáveis são usadas para armazenar valores de diferentes tipos de dados (inteiros, strings, floats, etc).
# Condicionais (if, elif, else) são usadas para controlar o fluxo do programa com base em condições lógicas.
# Loops (for, while) são usados para repetir blocos de código. O for itera sobre uma sequência, enquanto o while continua até que uma condição seja falsa.
# Strings e Listas:
# Strings são sequências de caracteres.
# Listas são coleções de valores que podem ser manipuladas (acessadas, modificadas, ordenadas, etc.).
# Funções (def) permitem organizar o código em blocos reutilizáveis e podem ter parâmetros e valores de retorno.