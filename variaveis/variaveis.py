# as variáveis são usadas para armazenar dados que podem ser utilizados durante a execução do programa.
# No exemplo abaixo, temos três variáveis: nome, idade e altura.
# Nome é uma string (texto), idade é um número inteiro e altura é um número decimal (float).

nome = "João"  # variável que armazena uma string
idade = 25      # variável que armazena um número inteiro
altura = 1.75   # variável que armazena um número decimal

print(nome)
print(idade)
print(altura)